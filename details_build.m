function fig_hdl = details_build(details)
% Initialize handles structure
handles = struct();

% Create all UI controls
build_gui();

% Assign function output
fig_hdl = handles.figure1;

%% ---------------------------------------------------------------------------
	function build_gui()
% Creation of all uicontrols

		% --- FIGURE -------------------------------------
		handles.figure1 = figure( ...
			'Tag', 'figure1', ...
			'Units', 'characters', ...
			'Position', [102.8 35.3846153846154 59 19.3076923076923], ...
			'Name', 'details', ...
			'MenuBar', 'none', ...
			'NumberTitle', 'off', ...
			'Color', get(0,'DefaultUicontrolBackgroundColor'));

		% --- PANELS -------------------------------------
		handles.uipanel1 = uipanel( ...
			'Parent', handles.figure1, ...
			'Tag', 'uipanel1', ...
			'Units', 'normalized', ...
			'Position', [0.0771929824561404 0.058091286307054 0.842105263157895 0.79253112033195], ...
			'BackgroundColor', [1 1 1], ...
			'Title', '');

		% --- STATIC TEXTS -------------------------------------
		handles.details = uicontrol( ...
			'Parent', handles.uipanel1, ...
			'Tag', 'details', ...
			'Style', 'text', ...
			'Units', 'normalized', ...
			'Position', [0.0593220338983051 0.0529100529100529 0.872881355932203 0.883597883597883], ...
			'BackgroundColor', [1 1 1], ...
			'String', '');


    end

set(handles.details,'String',details);

end
